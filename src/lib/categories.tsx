export function getCategoryIcon(category: string, className?: string): ReactNode {
  switch (category) {
    case 'food': return <FoodIcon className={className} />
    case 'transport': return <TransportIcon className={className} />
    case 'bills': return <BillsIcon className={className} />
    case 'shopping': return <ShoppingIcon className={className} />
    case 'entertainment': return <EntertainmentIcon className={className} />
    case 'health': return <HealthIcon className={className} />
    default: return <OtherIcon className={className} />
  }
}

export function getCategoryMeta(category: string): CategoryMeta {
  return CATEGORIES[category as CategoryType] || CATEGORIES.other
}
